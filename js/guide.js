// Tab switching
function switchTab(tabId) {
    // Update active tab
    document.querySelectorAll('.guide-tab').forEach(tab => {
        tab.classList.remove('active');
    });
    document.querySelector(`[data-tab="${tabId}"]`).classList.add('active');

    // Render content
    const contentDiv = document.getElementById('guide-content');
    let html = '';

    switch(tabId) {
        case 'books':
            html = renderBooks(guideData.books);
            break;
        case 'podcasts':
            html = renderPodcasts(guideData.podcasts);
            break;
        case 'technical':
            html = renderTips(guideData.technical, tabId);
            break;
        case 'mental-training':
            html = renderTips(guideData["mental-training"], tabId);
            break;
        case 'fitness':
            html = renderTips(guideData.fitness, tabId);
            break;
    }

    contentDiv.innerHTML = html;
    contentDiv.scrollIntoView({ behavior: 'smooth', block: 'start' });
}

// Event Listeners
document.querySelectorAll('.guide-tab').forEach(tab => {
    tab.addEventListener('click', () => {
        switchTab(tab.dataset.tab);
    });
});

// Initialize with books tab
document.addEventListener('DOMContentLoaded', () => {
    switchTab('books');
});
